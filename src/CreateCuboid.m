function [vertices, faces, edges] = CreateCuboid(Origin, Size)
% Adapted from:
% http://www.mathworks.com/matlabcentral/fileexchange/24484-geom3d/content/...
% geom3d/meshes3d/createCube.m

x0 = Origin(1); dx= Size(1)/2;
y0 = Origin(2); dy= Size(2)/2;
z0 = Origin(3); dz= Size(3)/2;

vertices = [...
    x0-dx y0-dy z0-dz; ...
    x0+dx y0-dy z0-dz; ...
    x0-dx y0+dy z0-dz; ...
    x0+dx y0+dy z0-dz; ...
    x0-dx y0-dy z0+dz; ...
    x0+dx y0-dy z0+dz; ...
    x0-dx y0+dy z0+dz; ...
    x0+dx y0+dy z0+dz];

edges = [1 2;1 3;1 5;2 4;2 6;3 4;3 7;4 8;5 6;5 7;6 8;7 8];

% faces are oriented such that normals point outwards
faces = [1 3 4 2;5 6 8 7;2 4 8 6;1 5 7 3;1 2 6 5;3 7 8 4];

end
